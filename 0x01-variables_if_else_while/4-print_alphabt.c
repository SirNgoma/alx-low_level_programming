#include <stdio.h>

/**
 *  * main - create alphabets using for loop and putchar
 *   * Return: prints lowercase alphabets
 *    */

int main() {
	    
	    int alpha;
	        
	        
	        for (alpha = 97; alpha <= 122; alpha++)
			    {
				                putchar(alpha);
						    
						    }
		    
		    return (0) ;
}
